import sqlite3
import json
import requests
from datetime import datetime

# ШАГ 1 — подключение к базе
def подключить():
    conn = sqlite3.connect("посты.db")
    conn.row_factory = sqlite3.Row
    return conn

# ШАГ 2 — SQL запрос → список словарей
def выполнить_запрос(conn, sql, параметры=()):
    cursor = conn.cursor()
    cursor.execute(sql, параметры)
    строки = cursor.fetchall()
    return [dict(строка) for строка in строки]

# ШАГ 3 — сохранить результат в JSON
def сохранить_json(данные, имя_файла):
    with open(имя_файла, "w", encoding="utf-8") as f:
        json.dump(данные, f, ensure_ascii=False, indent=4)
    print(f"Сохранено в {имя_файла}")

# ШАГ 4 — аналитические запросы
def сделать_аналитику(conn):
    
    # все посты
    все_посты = выполнить_запрос(conn, """
        SELECT id, автор_id, заголовок
        FROM посты
        ORDER BY id
    """)
    
    # статистика по авторам
    по_авторам = выполнить_запрос(conn, """
        SELECT 
            автор_id,
            COUNT(*) as постов,
            AVG(LENGTH(текст)) as средняя_длина
        FROM посты
        GROUP BY автор_id
        ORDER BY постов DESC
    """)
    
    # округляем среднюю длину
    for автор in по_авторам:
        автор["средняя_длина"] = round(автор["средняя_длина"], 1)
    
    # топ 3 длинных поста
    топ_длинные = выполнить_запрос(conn, """
        SELECT id, автор_id, заголовок, LENGTH(текст) as длина
        FROM посты
        ORDER BY длина DESC
        LIMIT 3
    """)
    
    # топ 3 коротких поста
    топ_короткие = выполнить_запрос(conn, """
        SELECT id, автор_id, заголовок, LENGTH(текст) as длина
        FROM посты
        ORDER BY длина ASC
        LIMIT 3
    """)
    
    # собираем итоговый отчёт
    отчёт = {
        "meta": {
            "дата": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            "всего_постов": len(все_посты),
            "всего_авторов": len(по_авторам)
        },
        "статистика_авторов": по_авторам,
        "топ_длинные_посты": топ_длинные,
        "топ_короткие_посты": топ_короткие
    }
    
    return отчёт

# ЗАПУСК
print("Запускаю аналитику...")

conn = подключить()
отчёт = сделать_аналитику(conn)

# выводим в терминал
print(f"\nВсего постов: {отчёт['meta']['всего_постов']}")
print(f"Всего авторов: {отчёт['meta']['всего_авторов']}")

print("\nТоп авторов:")
for автор in отчёт["статистика_авторов"][:3]:
    print(f"  Автор #{автор['автор_id']} — {автор['постов']} постов")

print("\nСамый длинный пост:")
длинный = отчёт["топ_длинные_посты"][0]
print(f"  #{длинный['id']}: {длинный['заголовок'][:50]}...")

# сохраняем в JSON
сохранить_json(отчёт, "полный_отчёт.json")

conn.close()
print("\nГотово!")