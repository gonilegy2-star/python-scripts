def сложить(a, b):
    return a + b

def вычесть(a, b):
    return a - b

def умножить(a, b):
    return a * b

def разделить(a, b):
    if b == 0:
        return "На ноль делить нельзя!"
    return a / b

print("Калькулятор запущен")
print("-" * 30)

while True:
    команда = input("Введи пример (или 'стоп'): ")
    
    if команда.lower() == "стоп":
        print("Выход!")
        break
    
    try:
        результат = eval(команда, {"__builtins__": None}, {
            "сложить": сложить,
            "вычесть": вычесть,
            "умножить": умножить,
            "разделить": разделить
        })
        print(f"Результат: {результат}")
    except Exception:
        print("Не понимаю, попробуй: сложить(5, 3)")