import pandas as pd
import json

# создаём данные
df = pd.DataFrame({
    "имя":     ["Данил", "Иван", "Мария", "Алексей", "Ольга", "Дмитрий", "Анна"],
    "возраст": [20, 25, 22, 30, 28, 35, 19],
    "город":   ["Москва", "Питер", "Москва", "Казань", "Питер", "Москва", "Казань"],
    "баланс":  [1500, 3200, 800, 5000, 1200, 7500, 500],
    "активен": [True, True, False, True, False, True, True]
})

print("=== ИСХОДНЫЕ ДАННЫЕ ===")
print(df)

print("\n=== ФИЛЬТРАЦИЯ ===")
москвичи = df[df["город"] == "Москва"]
print("Москвичи:")
print(москвичи)

богатые_активные = df[(df["баланс"] > 2000) & (df["активен"] == True)]
print("\nАктивные с балансом > 2000:")
print(богатые_активные[["имя", "баланс"]])

print("\n=== СОРТИРОВКА ===")
по_балансу = df.sort_values("баланс", ascending=False)
print("По убыванию баланса:")
print(по_балансу[["имя", "баланс"]])

print("\n=== GROUPBY ===")
по_городам = df.groupby("город").agg(
    количество    = ("имя",     "count"),
    сумма_балансов = ("баланс", "sum"),
    средний_баланс = ("баланс", "mean"),
    средний_возраст = ("возраст", "mean")
).round(1)
print(по_городам)

print("\n=== PIVOT TABLE ===")
сводная = pd.pivot_table(
    df,
    values="баланс",
    index="город",
    columns="активен",
    aggfunc="sum",
    fill_value=0
)
print(сводная)

print("\n=== ДОБАВЛЯЕМ СТОЛБЕЦ ===")
df["статус"] = df["баланс"].apply(
    lambda x: "VIP" if x > 3000 else "обычный"
)
print(df[["имя", "баланс", "статус"]])

# сохраняем результат в JSON
результат = {
    "всего_людей": len(df),
    "средний_баланс": round(df["баланс"].mean(), 2),
    "самый_богатый": df.loc[df["баланс"].idxmax(), "имя"],
    "по_городам": по_городам.to_dict()
}

with open("pandas_результат.json", "w", encoding="utf-8") as f:
    json.dump(результат, f, ensure_ascii=False, indent=4)

print("\nСохранено в pandas_результат.json")
import pandas as pd
import numpy as np

# грязные данные
df = pd.DataFrame({
    "имя":      ["Данил", "Иван", "Мария", "Иван", "Алексей", "Ольга", None, "Данил"],
    "возраст":  [20, 25, None, 25, 300, 28, 22, 20],
    "город":    ["Москва", "питер", "МОСКВА", "питер", "Казань", None, "Москва", "Москва"],
    "баланс":   [1500, 3200, 800, 3200, 999999, None, 1200, 1500],
    "дата":     ["2024-01-15", "2024-02-20", "2024-01-15", "2024-02-20",
                 "плохая дата", "2024-03-10", "2024-01-20", "2024-01-15"],
    "зарплата": ["50000", "75000", "45000", "75000", "100000", "60000", "55000", "50000"]
})

print(f"До чистки: {df.shape}")

# 1. дубликаты
df = df.drop_duplicates()
print(f"После удаления дублей: {df.shape}")

# 2. NaN
df = df.dropna(subset=["имя"])
df["баланс"]  = df["баланс"].fillna(df["баланс"].median())
df["возраст"] = df["возраст"].fillna(df["возраст"].median())
df["город"]   = df["город"].fillna("Не указан")

# 3. типы
df["зарплата"] = pd.to_numeric(df["зарплата"], errors="coerce")
df["дата"]     = pd.to_datetime(df["дата"], errors="coerce")

# 4. строки — привести к одному виду
df["город"] = df["город"].str.title()

# 5. выбросы
df = df[df["возраст"] < 100]
df = df[df["баланс"] < 100000]

# 6. apply — новые столбцы
df["категория"] = df["баланс"].apply(
    lambda x: "VIP" if x > 3000 else "обычный"
)

# 7. pd.cut — возрастные группы
df["возрастная_группа"] = pd.cut(
    df["возраст"],
    bins=[0, 25, 35, 100],
    labels=["молодой", "средний", "старший"]
)

# 8. даты
df["месяц"] = df["дата"].dt.month
df["год"]   = df["дата"].dt.year

print(f"\nПосле чистки: {df.shape}")
print("\nЧистые данные:")
print(df)
print("\nТипы:")
print(df.dtypes)