import json
from datetime import datetime

# память бота — хранится пока скрипт работает
память = {
    "имя_пользователя": None,
    "количество_сообщений": 0,
    "история": []
}

def сохранить_историю():
    with open("история_бота.json", "w", encoding="utf-8") as f:
        json.dump(память["история"], f, ensure_ascii=False, indent=4)

def добавить_в_историю(от_кого, текст):
    запись = {
        "от": от_кого,
        "текст": текст,
        "время": datetime.now().strftime("%H:%M:%S")
    }
    память["история"].append(запись)

def обработать(сообщение):
    текст = сообщение.lower().strip()
    память["количество_сообщений"] += 1

    # команда "посчитай 5 + 3" — бот считает пример
    if текст.startswith("посчитай "):
        пример = сообщение[9:]
        try:
            результат = eval(пример)
            return f"Результат: {результат}"
        except:
            return "Не могу посчитать это"

    # команда "повтори ТЕКСТ" — бот повторяет
    if текст.startswith("повтори "):
        повтор = сообщение[8:]
        return повтор

    # команда "сколько слов ТЕКСТ" — бот считает слова
    if текст.startswith("сколько слов "):
        фраза = сообщение[13:]
        количество = len(фраза.split())
        return f"В тексте {количество} слов"

    return "Не понял команду"

# основной цикл бота
while True:
    пользователь_ввод = input("Ты: ")
    if пользователь_ввод.lower() == "выход":
        сохранить_историю()
        break
    
    ответ = обработать(пользователь_ввод)
    добавить_в_историю("пользователь", пользователь_ввод)
    добавить_в_историю("бот", ответ)
    print(f"Бот: {ответ}")